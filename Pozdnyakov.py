def greeting_bot():
    """Бот для приветствия пользователей"""
    
    print("Добро пожаловать в программу приветствия!")
    print("Введите 'exit' для выхода")
    print("-" * 40)
    
    while True:
        # Запрос имени пользователя
        name = input("\nКак тебя зовут? ").strip()
        
        # Проверка на выход
        if name.lower() == 'exit':
            print("До свидания! Было приятно пообщаться! ")
            break
        
        # Проверка пустого имени
        if not name:
            print("Кажется, ты забыл ввести имя! ")
            continue
        
        # Приветствие пользователя
        print(f"Привет, {name}! Рад тебя видеть! ")
        
        # Дополнительное сообщение в зависимости от длины имени
        if len(name) > 10:
            print("У тебя такое длинное имя! ")
        elif len(name) < 3:
            print("Короткое и милое имя! ")

# Запуск программы
greeting_bot()